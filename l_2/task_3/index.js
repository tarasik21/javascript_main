let a = 5; // Переменной a присваивается значение 5.
let c = 10; // Переменной c присваивается значение 10.
a = c; // Переменной a присваивается значение переменной c (10).

let b = [1, 2, 3]; // Переменной b присваивается массив [1, 2, 3].
b.push(10); // В массив добавляется число 10, массив становится [1, 2, 3, 10].

console.log(a); // Выводит значение переменной a (10).
console.log(b); // Выводит массив b ([1, 2, 3, 10]).

